public class SwitchCase2{
    public static void main(String[] args){
        int i = 2;
        switch ( i )
        {
            case 1:
            System.out.println("The number is 1");
            break;
            
            case 2:
            System.out.println("The number is 2");
            break;
            
            default:
            System.out.println("The number is greater than 2");
            
        }
                
    }
}